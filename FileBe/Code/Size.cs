
namespace FileBe
{
    public class Size
    {
        public double x { get; set; }
        public double y { get; set; }
        public Size()
        {
            x = 0; y = 0;
        }
        public Size(double _x, double _y)
        {
            x = _x;
            y = _y;
        }
    }
}
